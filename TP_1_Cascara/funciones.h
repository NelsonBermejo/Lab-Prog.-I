#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/** \brief Suma dos numeros flotantes
 *
 * \param op1 Primer Operando
 * \param op2 Segundo Operando
 * \return Devuelve suma de dos operandos
 *
 */
float Sumar(float, float);

/** \brief Resta dos numeros flotantes
 *
 * \param op1 Primer Operando
 * \param op2 Segundo Operando
 * \return Devuelve resta de dos operandos
 *
 */
float Restar(float, float);

/** \brief Divide dos numeros flotantes
 *
 * \param op1 Primer Operando
 * \param op2 Segundo Operando
 * \return Devuelve division de dos operandos
 *
 */
float Dividir(float, float);

/** \brief Multiplica dos numeros flotantes
 *
 * \param op1 Primer Operando
 * \param op2 Segundo Operando
 * \return Devuelve producto de dos operandos
 *
 */
float Multiplicar(float, float);

/** \brief Calcula factorial de un numero
 *
 * \param N Numero Natural
 * \return Devuelve factorial de un numero
 *
 */
int Factorial(int);

#endif // FUNCIONES_H_INCLUDED
